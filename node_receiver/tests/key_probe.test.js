import test from "node:test";
import assert from "node:assert/strict";

import { buildFrame, buildPlainForEncrypt } from "../src/protocol.js";
import { buildKeyCandidates, parseHexDump, probeDatagramKey } from "../src/key_probe.js";
import { xteaEncryptEcbLE } from "../src/xtea.js";

test("buildKeyCandidates includes teleofis and zero-password defaults", () => {
  const candidates = buildKeyCandidates({
    imei: "867724030459827",
    pin: "1964",
  });
  const keyHexes = new Set(candidates.map((candidate) => candidate.keyHex));

  assert.ok(keyHexes.has(Buffer.from("teleofis", "ascii").toString("hex") + "0000000000000000"));
  assert.ok(keyHexes.has("00000000000000000000000000000000"));
});

test("probeDatagramKey finds padded teleofis key on synthetic packet", () => {
  const imei = "867724030459827";
  const payload = Buffer.from("0900", "hex");
  const key = Buffer.alloc(16, 0x00);
  Buffer.from("teleofis", "ascii").copy(key, 0);

  const datagram = buildFrame(imei, xteaEncryptEcbLE(buildPlainForEncrypt(payload), key));
  const result = probeDatagramKey(datagram, {
    pin: "1964",
  });

  assert.equal(result.imei, imei);
  assert.equal(result.matches.length, 1);
  assert.equal(result.matches[0].key_hex, key.toString("hex"));
  assert.equal(result.matches[0].source, "login:ascii_padded");
  assert.deepEqual(result.matches[0].records.map((record) => record.id), [9]);
});

